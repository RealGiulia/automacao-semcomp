function preenche_tabela(tabela, registros, periodo) {
    var tabela_header = "<tr><th>Descricao</th><th>Data</th><th>Valor</th></tr>"
    var linhas = ''

    var hoje = new Date();
    var options = {year: 'numeric', month: '2-digit', day:'numeric',hour:'numeric', minute:'numeric', second:'numeric'};

    for (var i in registros) {
        var descricao = registros[i]['Descricao']
        var data = new Date(registros[i]['Data'])
        var valor =  registros[i]['Valor'].toFixed(2)
        var linha = ''
        console.log(periodo.value)
        if (periodo.value == 'mes-atual') {
            var mes_atual = new Date(hoje.getFullYear(), hoje.getMonth(), 1);
            if (data >= mes_atual) {
                linha = `<tr><td>${descricao}</td><td>${data.toLocaleDateString('pt-BR', options)}</td><td>${valor}</td></tr>`
            }
        }
        else if (periodo.value == 'seis-meses') {
            var seis_meses = new Date(hoje.getFullYear(), hoje.getMonth()-6, hoje.getDay());
            if (data >= seis_meses) {
                linha = `<tr><td>${descricao}</td><td>${data.toLocaleDateString('pt-BR', options)}</td><td>${valor}</td></tr>`
            }
        }
        else if (periodo.value == 'ano-atual') {
            var ano_atual = new Date(hoje.getFullYear(), 1, 1);
            if (data >= ano_atual) {
                linha = `<tr><td>${descricao}</td><td>${data.toLocaleDateString('pt-BR', options)}</td><td>${valor}</td></tr>`
            }
        }
        else if (periodo.value == 'total') {
            linha = `<tr><td>${descricao}</td><td>${data.toLocaleDateString('pt-BR', options)}</td><td>${valor}</td></tr>`
        }
        linhas = linhas + linha
    }
    tabela.innerHTML = tabela_header + linhas
}

preenche_tabela(tabela, registros, periodo)

periodo.addEventListener('change', () => {preenche_tabela(tabela, registros, periodo)})